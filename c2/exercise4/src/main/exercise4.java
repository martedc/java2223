package main;

public class exercise4 {
	public static void main(String[] args) {

		// An application that converts 37 degrees Fahrenheit to Celsius.

		int fahrenheit = 37; // Fahrenheit
		double a = 1.8;
		int b = 32;
		double celsius; // Celsius

		celsius = (fahrenheit-b)/a;

		System.out.println(fahrenheit + " degrees Fahrenheit equal " + celsius + " degrees Celsius.");

		}
}
